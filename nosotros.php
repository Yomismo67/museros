          <li><b>Juego:</b> Sacar los labios cerrados hacia afuera. (No vale para la treinta y una.)</li>
          <li><b>Treinta y una:</b> Guiñar un ojo.</li>
          <li><b>Treinta:</b> Alzar los dos hombros. Guiñar un ojo después de cantar juego no, los cuatro jugadores.</li>
          <li><b>Veintinueve:</b> Alzar un hombro.</li>
          <li><b>Ciego:</b> Cerrar los dos ojos. (Esta seña no se podrá pasar si hay jugada que tenga seña establecida.)</li>
          <li><b>Cortar el mus:</b> Inclinar la cabeza hacia un lado.</li>
          <li><b>Envidar más:</b> Levantar la cabeza.</li>
