        <h1>Artículo 8. Significado de los siguientes vocablos:</h1>

        <ul>
          <li><b>Mus:</b> Pedir descarte.
          <li><b>Paso:</b> Cortar el mus si se es mano. Pasar el envite. No aceptar el envite.
          <li><b>Envido:</b> Apostar dos tantos. Si a la palabra envido se le une un número, ese número es el valor de la apuesta.
          <li><b>Reenvido:</b> Doblar el valor de la apuesta.
          <li><b>Órdago:</b> Apostar el juego completo.
          <li><b>Mano:</b> El jugador a la derecha del que reparte. En pares o juego el primer jugador que los tiene es mano del lance.
          <li><b>Jugada:</b> La forman los cuatro naipes que se reciben para jugar.
          <li><b>Lance:</b> Una de las cuatro suertes de tanteo de una jugada (grande, chica, pares, juego o punto).
          <li><b>Negada:</b> Tanto ganado por la no aceptación de cualquier primera apuesta en un lance.
          <li><b>Deje:</b> Tanto sumado a los ya ganados como consecuencia de un revoque no aceptado en pares, juego o punto.
          <li><b>No Juego:</b> Cuando los cuatro naipes suman menos de treinta y uno (Equivalente a punto.)
        </ul> <br>
